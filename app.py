def document_chat():
    st.title("Notesight POC - Document QA")
    selected_model = st.selectbox("ğŸ¤– Select AI Model", ["Gemini", "ChatGPT"], index=0)
    uploaded_file = st.file_uploader("Upload a File")
    if uploaded_file and st.button("ğŸ“¤ Upload File"):
        response = api_request("upload", files={"file": (uploaded_file.name, uploaded_file, "application/pdf")}, data={"model": selected_model.lower()})
        if response:
            st.success("âœ… File uploaded successfully!")
            st.session_state.file_path = response.get("file_path")
    st.subheader("ğŸ’¬ Ask Questions About the Document")
    query = st.chat_input("Ask a question...")
    if query:
        response = api_request("ask", data={"query": query, "model": selected_model.lower()})
        answer = response.get("answer", "âš  No response received.") if response else "âŒ Failed to get a response."
        st.session_state.messages.append({"role": "user", "content": query})
        st.session_state.messages.append({"role": "assistant", "content": answer})
        for message in st.session_state.messages:
            with st.chat_message(message["role"]):
                st.markdown(message["content"])

def report_card():
    st.title("Student Report Generator")
    uploaded_file = st.file_uploader("Upload a PDF file", type=["pdf"])
    st.session_state.report=[]
    if uploaded_file and st.button("Generate Report"):
        files = {"file": uploaded_file.getvalue()}
        response = requests.post(f"{BASE_URL}/report/", files=files)
        if response.status_code==200:
            st.session_state.report = response.get("data")
        else: st.error("Failed to generate report")
    if st.session_state.report:
        report = st.session_state.report
        if isinstance(report, dict):
            if report['student_info']['name'] == "Unknown" and report['student_info']['roll_number'] == "Unknown":
                st.error("âŒ Failed to generate report")
            else:
                st.subheader("Generated Report")
                st.write(f"**Student Name:** {report['student_info']['name']}")
                st.write(f"**Roll Number:** {report['student_info']['roll_number']}")
                st.write(f"**Grade:** {report['student_info']['grade']}")
                st.write(f"**School:** {report['student_info']['school']}")
                st.subheader("Subject Performance")
                for subject, details in report["subject_performance"].items():
                    st.write(f"- **{subject}:** {details.get('final_grade', 'N/A')}")
                st.subheader("Strengths")
                st.write(", ".join(report["strengths"]) if report["strengths"] else "No strengths identified.")

                st.subheader("Weaknesses")
                for weakness in report["weaknesses"]:
                    st.write(f"- **{weakness['subject']}**: {weakness['reason']}")

                st.subheader("Overall Performance Summary")
                st.write(report["overall_performance_summary"])
        else:
            st.error("âŒ Failed to generate a structured report. Response received:")
            st.text_area("Response", report, height=200)

def mcq_generation():
    st.title("ğŸ“˜ Notesight POC - Generate MCQs")
    uploaded_files = st.file_uploader("ğŸ“‚ Upload PDFs for MCQ generation", type=["pdf"], accept_multiple_files=True)
    MODEL_OPTIONS = {"Gemini": "gemini", "ChatGPT": "chatgpt", "Mistral": "mistral"}
    selected_model = st.selectbox("ğŸ¤– Select AI Model", list(MODEL_OPTIONS.keys()))
    selected_model_key = MODEL_OPTIONS[selected_model]

    if "mcqs" not in st.session_state:
        st.session_state["mcqs"] = []

    if "mcq_answers" not in st.session_state:
        st.session_state["mcq_answers"] = {}

    if uploaded_files and st.button("ğŸ” Extract Topics"):
        files = [("files", (file.name, file, "application/pdf")) for file in uploaded_files]
        with st.spinner(f"Extracting topics using {selected_model.capitalize()}... â³"):
            response = requests.post(f"{BASE_URL}/mcqs/", files=files, data={"model": selected_model_key})
            if response.status_code == 200:
                st.session_state["topics_hierarchy"] = response.json().get("topics", {})
                st.success("âœ… Topics Extracted! Select subtopics below.")
                st.session_state["file_paths"] = response.json().get("file_paths", [])
            else:
                st.error("âŒ Failed to extract topics.")

    if st.session_state.get("topics_hierarchy"):
        st.subheader("ğŸ“‘ Select Subtopics for MCQ Generation")
        selected_subtopics = []
        for chapter, subtopics in st.session_state["topics_hierarchy"].items():
            with st.expander(f"ğŸ“– {chapter}"):
                chapter_selected = st.checkbox(f"Select All in {chapter}", key=f"{chapter}_all")
                for subtopic in subtopics:
                    subtopic_selected = st.checkbox(subtopic, key=f"{chapter}_{subtopic}", value=chapter_selected)
                    if subtopic_selected:
                        selected_subtopics.append(subtopic)
        st.session_state["selected_subtopics"] = selected_subtopics

    if st.session_state.get("selected_subtopics") and st.button("ğŸ¯ Generate MCQs"):
        with st.spinner(f"Generating MCQs using {selected_model.capitalize()}... â³"):
            response = requests.post(
                f"{BASE_URL}/mcqs/generate/",
                json={"topics": st.session_state["selected_subtopics"], "file_paths": st.session_state["file_paths"], "model": selected_model_key}
            )

        if response.status_code == 200:
            st.session_state["mcqs"] = response.json()
            st.session_state["mcq_answers"] = {}
    
    if uploaded_files and st.button("ğŸ¯ Generate Personalized MCQs"):
        token = st.session_state.get("token")
        if not token:
            st.error("âŒ Please log in first!")
            return

        headers = {"Authorization": f"Bearer {token}"}
        files = [("files", (file.name, file, "application/pdf")) for file in uploaded_files]
        data = {"model":selected_model_key}

        with st.spinner("Generating Personalized MCQs... â³"):
            response = requests.post(f"{BASE_URL}/mcqs/personalized/", files=files, data=data, headers=headers)

        if response.status_code == 200:
            st.session_state["mcqs"] = response.json()
            st.session_state["mcq_answers"] = {}
        else: st.error("failed to generate mcqs")
    st.session_state.setdefault("mcq_answers", {})
    if st.session_state["mcqs"]:
        st.subheader("ğŸ“š Generated MCQs")

        for i, mcq in enumerate(st.session_state["mcqs"]):
            with st.expander(f"ğŸ“ {mcq['question']}"):
                with st.form(key=f"mcq_form_{i}"):
                    selected_option = st.radio(
                    "Select your answer:",
                    mcq["options"],
                    key=f"mcq_{i}"
                    )
                    submit_button = st.form_submit_button("Check Answer")
                    if submit_button:
                        st.session_state["mcq_answers"][i] = selected_option
                        correct_answer = mcq["correct_answer"]
                        selected_letter = selected_option.split(".")[0]
                        if selected_letter.lower() == correct_answer.lower():   
                            st.success("âœ… Correct!")
                        else:
                            st.error(f"âŒ Incorrect! The correct answer is: **{correct_answer}**")

def get_user_data():
    """Fetch user profile data from the API."""
    headers = {"Authorization": f"Bearer {st.session_state.token}"}
    response = requests.get(f"{BASE_URL}/profile/", headers=headers)
    return response.json() if response.status_code == 200 else None

def show_profile_info(user_data):
    """Display user profile details."""
    st.subheader("Profile Information")
    st.text_input("Username", value=user_data.get("username"), disabled=True)

def show_report_details(latest_report):
    """Toggle and display student report details."""
    if not latest_report:
        st.info("No report available. Please upload one below.")
        # Add file uploader for PDF
        uploaded_file = st.file_uploader("Upload a PDF report", type=["pdf"])
        if uploaded_file is not None:
            # Call the API to upload the report
            headers = {"Authorization": f"Bearer {st.session_state.token}"}
            files = {"file": (uploaded_file.name, uploaded_file, "application/pdf")}
            response = requests.post(f"{BASE_URL}/report-profile/", headers=headers, files=files)
            
            if response.status_code == 200:
                st.success("Report uploaded successfully! Refresh the page to see the updated report.")
            else:
                st.error(f"Failed to upload report: {response.json().get('detail', 'Unknown error')}")
        return  # Exit early if no report exists yet

    if "show_report" not in st.session_state:
        st.session_state.show_report = False

    if st.button("ğŸ“„ Show Report" if not st.session_state.show_report else "ğŸ”½ Hide Report"):
        st.session_state.show_report = not st.session_state.show_report

    if st.session_state.show_report:
        st.subheader("ğŸ“„ Latest Report")
        student_info = latest_report.get("student_info", {})
        st.write(f"**Student Name:** {student_info.get('name', 'N/A')}")
        st.write(f"**Roll Number:** {student_info.get('roll_number', 'N/A')}")
        st.write(f"**Grade:** {student_info.get('grade', 'N/A')}")
        st.write(f"**School:** {student_info.get('school', 'N/A')}")

        st.subheader("ğŸ“Š Subject Performance")
        for subject, details in latest_report.get("subject_performance", {}).items():
            st.write(f"- **{subject}:** {details.get('final_grade', 'N/A')}")

        st.subheader("ğŸ’¡ Strengths")
        strengths = latest_report.get("strengths", [])
        st.write(", ".join(strengths) if strengths else "No strengths identified.")

        st.subheader("âš  Weaknesses")
        for weakness in latest_report.get("weaknesses", []):
            st.write(f"- **{weakness['subject']}**: {weakness['reason']}")

        st.subheader("ğŸ“Œ Overall Performance Summary")
        st.write(latest_report.get("overall_performance_summary", "No summary available."))
def profile():
    """Main profile function."""
    st.title("ğŸ‘¤ User Profile")

    if not st.session_state.get("token"):
        st.warning("You need to be logged in to view your profile.")
        return

    user_data = get_user_data()
    if not user_data:
        st.error("âŒ Failed to fetch profile details.")
        return

    show_profile_info(user_data)
    show_report_details(user_data.get("latest_report"))

